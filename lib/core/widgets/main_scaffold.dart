import 'package:carporater/core/widgets/gradient.dart';
import 'package:carporater/core/widgets/appbar.dart';
import 'package:flutter/material.dart';



class MainScaffold extends StatefulWidget {
  final Widget body;
  final String title;

  const MainScaffold({
    super.key,
    required this.body,
    required this.title,
  });

  @override
  State<MainScaffold> createState() => _MainScaffoldState();
}

class _MainScaffoldState extends State<MainScaffold> {

  bool isMenuOpen = false;

  void toggleMenu() {
    setState(() => isMenuOpen = !isMenuOpen);
  }

  @override
  Widget build(BuildContext context) {

    final width = MediaQuery.of(context).size.width;
    final menuWidth = width * 0.65;

    return Scaffold(
      body: Stack(
        children: [

          Scaffold(
            appBar: appBar(
              context,
              title: widget.title,
              hamburger: true,
              onMenuPressed: toggleMenu,
            ),

            body: widget.body,
          ),

          if (isMenuOpen)
            GestureDetector(
              onTap: toggleMenu,
              child: Container(
                color: Colors.black.withOpacity(0.4),
              ),
            ),

          AnimatedPositioned(
            duration: const Duration(milliseconds: 300),
            left: isMenuOpen ? 0 : -menuWidth,
            top: 0,
            bottom: 0,
            child: Container(
              width: menuWidth,
              decoration: const BoxDecoration(
                gradient: AppGradients.glowGradient,
                color: Colors.white,
                borderRadius: BorderRadius.horizontal(
                  right: Radius.circular(20),
                ),
                boxShadow: [
                  BoxShadow(color: Colors.black26, blurRadius: 15),
                ],
              ),
              child: SafeArea(
                child: Column(
                  children: [

                    const SizedBox(height: 20),
                    const CircleAvatar(radius: 60,backgroundColor: Colors.white,),
                    const SizedBox(height: 10),
                    const Text(
                      "User Name",
                      style:
                          TextStyle(fontSize: 18, fontWeight: FontWeight.bold,color: Colors.white),
                    ),

                    const Divider(),
                    menuItem(Icons.analytics_rounded, "Dashboard"),
                    menuItem(Icons.person, "Profile"),
                    menuItem(Icons.admin_panel_settings_outlined, "Password"),
                    menuItem(Icons.article, "Complaints"),
                    menuItem(Icons.all_inbox_rounded, "Complaint History"),
                    menuItem(Icons.assignment_turned_in_rounded, "Resolved Issues"),
                    ElevatedButton(
                      style: ElevatedButton.styleFrom(
                        backgroundColor: Colors.red,                        
                      ),
                      onPressed: (){},
                      child: Padding(
                        padding: EdgeInsetsGeometry.all(10),
                        child: Row(
                          children: [
                            Icon(Icons.logout_rounded,color: Colors.white,),
                            SizedBox(width : 5),
                           const Text("Logout",
                           style: TextStyle(
                            fontSize: 24,
                            fontWeight: FontWeight(16),
                            color: Colors.white,
                           ),
                           )
                          ],
                        ),),

                      )
                  ],
                ),
              ),
            ),
          ),
        ],
      ),
    );
  }

  Widget menuItem(IconData icon, String title) {
    return ListTile(
      leading: Icon(icon ,color: Colors.white,),
      title: Text(title,
      style: TextStyle(
        color: Colors.white,
      ),),
      onTap: () {},
    );
  }
}

